package com.myxiaofeng.base;

public abstract class Person {//äºº
    private String name;
    private Integer age;

    public Person(String name, Integer age) {
        this.name = name;
        this.age = age;
    }

    public String getName() {
        return name;
    }

    public Integer getAge() {
        return age;
    }

    abstract void sayHello();
}
